/**
 * Generates styles/katex.css from node_modules/katex/dist/katex.css.
 *
 * Transforms:
 *  - Rewrites font URLs to use the inkdrop:// protocol
 *  - Strips WOFF and TTF font sources (Inkdrop's Chromium supports WOFF2)
 *  - Replaces `body` counter-reset with `.mde-preview` (Inkdrop-specific)
 *  - Wraps the `.katex`/`.katex-display` rules in `.inkdrop-math { ... }`,
 *    matching the className the React component renders on each math block,
 *    relying on native CSS nesting (baseline since Chromium 112) rather than
 *    a build-time flattener — Inkdrop's Electron ships a far newer Chromium.
 *    `@font-face` can't be nested, so it stays outside the wrapper.
 */

const fs = require('fs')
const path = require('path')

const ROOT = path.resolve(__dirname, '..')
const SRC = path.join(ROOT, 'node_modules', 'katex', 'dist', 'katex.css')
const DEST = path.join(ROOT, 'styles', 'katex.css')

const FONT_URL_PREFIX = 'inkdrop://math/node_modules/katex/dist/fonts'
const HEADER = '/* AUTO-GENERATED by scripts/generate-katex-css.js — DO NOT EDIT */\n'
const SCOPE_CLASS = '.inkdrop-math'

if (!fs.existsSync(SRC)) {
  console.warn(
    'generate-katex-css: katex.css not found at',
    SRC,
    '— skipping (run again after install)'
  )
  process.exit(0)
}

const src = fs.readFileSync(SRC, 'utf8')

const transformed = src
  // Rewrite font paths: url(fonts/...) → url("inkdrop://math/...")
  .replace(/url\(fonts\/([^)]+)\)/g, (_, filename) => `url("${FONT_URL_PREFIX}/${filename}")`)
  // Remove WOFF sources: , url("...woff") format("woff")
  .replace(/,\s*url\("[^"]*\.woff"\)\s*format\("woff"\)/g, '')
  // Remove TTF sources: , url("...ttf") format("truetype")
  .replace(/,\s*url\("[^"]*\.ttf"\)\s*format\("truetype"\)/g, '')
  // Replace body counter-reset with .mde-preview (Inkdrop-specific)
  .replace(
    /^body\s*\{[^}]*counter-reset:\s*katexEqnNo\s+mmlEqnNo[^}]*\}/m,
    '.mde-preview {\n  counter-reset: katexEqnNo mmlEqnNo;\n}'
  )

// Split off the `.katex`/`.katex-display` rules (everything between the last
// @font-face block and the trailing .mde-preview rule) so they can be nested
// under .inkdrop-math without touching @font-face or the counter-reset rule.
const firstSelectorIndex = transformed.search(/^\.[^\n{}]*\{/m)
const trailingRuleIndex = transformed.search(/^\.mde-preview\s*\{/m)
if (firstSelectorIndex === -1 || trailingRuleIndex === -1) {
  throw new Error('generate-katex-css: could not locate the KaTeX rule block to scope')
}

const preamble = transformed.slice(0, firstSelectorIndex)
const katexRules = transformed
  .slice(firstSelectorIndex, trailingRuleIndex)
  .trimEnd()
  .split('\n')
  .map(line => (line ? `  ${line}` : line))
  .join('\n')
const trailingRule = transformed.slice(trailingRuleIndex)

const output =
  HEADER + preamble + `${SCOPE_CLASS} {\n${katexRules}\n}\n\n` + trailingRule.trimEnd() + '\n'

// Sanity check
if (/url\(fonts\//.test(output)) {
  console.warn('generate-katex-css: warning: unrewritten font URLs remain')
}

fs.writeFileSync(DEST, output, 'utf8')
