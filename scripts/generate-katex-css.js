/**
 * Generates styles/katex.css from node_modules/katex/dist/katex.css.
 *
 * Transforms:
 *  - Rewrites font URLs to use the inkdrop:// protocol
 *  - Strips WOFF and TTF font sources (Inkdrop's Chromium supports WOFF2)
 *  - Replaces `body` counter-reset with `.mde-preview` (Inkdrop-specific)
 */

const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const SRC = path.join(
  ROOT,
  "node_modules",
  "katex",
  "dist",
  "katex.css"
);
const DEST = path.join(ROOT, "styles", "katex.css");

const FONT_URL_PREFIX = "inkdrop://math/node_modules/katex/dist/fonts";
const HEADER =
  "/* AUTO-GENERATED by scripts/generate-katex-css.js — DO NOT EDIT */\n";

if (!fs.existsSync(SRC)) {
  console.warn(
    "generate-katex-css: katex.css not found at",
    SRC,
    "— skipping (run again after install)"
  );
  process.exit(0);
}

const src = fs.readFileSync(SRC, "utf8");

const transformed = src
  // Rewrite font paths: url(fonts/...) → url("inkdrop://math/...")
  .replace(
    /url\(fonts\/([^)]+)\)/g,
    (_, filename) => `url("${FONT_URL_PREFIX}/${filename}")`
  )
  // Remove WOFF sources: , url("...woff") format("woff")
  .replace(/,\s*url\("[^"]*\.woff"\)\s*format\("woff"\)/g, "")
  // Remove TTF sources: , url("...ttf") format("truetype")
  .replace(/,\s*url\("[^"]*\.ttf"\)\s*format\("truetype"\)/g, "")
  // Replace body counter-reset with .mde-preview (Inkdrop-specific)
  .replace(
    /^body\s*\{[^}]*counter-reset:\s*katexEqnNo\s+mmlEqnNo[^}]*\}/m,
    ".mde-preview {\n  counter-reset: katexEqnNo mmlEqnNo;\n}"
  );

// Sanity checks
if (/url\(fonts\//.test(transformed)) {
  console.warn("generate-katex-css: warning: unrewritten font URLs remain");
}
if (/^body\s*\{/m.test(transformed)) {
  console.warn(
    "generate-katex-css: warning: body selector was not replaced"
  );
}

const output = HEADER + transformed.trimEnd() + "\n";

fs.writeFileSync(DEST, output, "utf8");
